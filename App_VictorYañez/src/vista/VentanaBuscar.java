    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // boton buscar por tipo de producto

        String nombre = this.txtNombre.getText().trim();

        ArrayList<Juego> lista
        = new RegistroJuego().buscarPorNombre(nombre);

        if (lista.isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(this,
                "NO hay registros de " + nombre);

        } else {

            // desplegamos la informaci√≥n en la tabla
            DefaultTableModel modelo = new DefaultTableModel();

            modelo.addColumn("id_juego");

            modelo.addColumn("Nombre");

            modelo.addColumn("Descripcion");

            modelo.addColumn("Precio");

            modelo.addColumn("Es multijugador");
            modelo.addColumn("Oferta");
            modelo.addColumn("TIPO DE JUEGO");


            for (Juego prod : lista) {

                Object[] fila = new Object[7];

                fila[0] = prod.getId_juego();

                fila[1] = prod.getNombre_juego();

                fila[2] = prod.getDescripcion();

                fila[3] = prod.getPrecio_juego();

                fila[4] = prod.isEs_multijugador();
                fila[5] = prod.isOferta();
                fila[6] = prod.getTipoJuego();


                modelo.addRow(fila);

            }

            this.tbSalida.setModel(modelo);

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

