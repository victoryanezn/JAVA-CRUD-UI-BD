    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        // vamos a crear un objeto Producto
        Juego juego = new Juego();
        // los "seteamos", es decir, cargamos los datos con los setter
        juego.setId_juego(this.txtId_juego.getText().trim().toUpperCase());
        juego.setNombre_juego(this.txtNombre_juego.getText().trim());
        juego.setDescripcion(this.txtDescripcion.getText().trim());
        juego.setPrecio_juego(Integer.parseInt(
                this.txtPrecio_juego.getText().trim()));
        // la informaci贸n de los radioButton
        if (this.rdEs_multijugador.isSelected()) {
            juego.setEs_multijugador(true);
        } else {
            juego.setEs_multijugador(false);
        }
        if (this.rdOferta.isSelected()) {
            juego.setOferta(true);
        } else {
            juego.setOferta(false);
        }
        juego.setTipoJuego(this.cmbTIpoJuego.getSelectedItem().toString());

        // ahora grabamos a traves del controlador
        if (new RegistroJuego().agregarJuego(juego)) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Juego Agregado a la base de datos...");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "NO Grab贸!!!");
        }

    }//GEN-LAST:event_btnGrabarActionPerformed

